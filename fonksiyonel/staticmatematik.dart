class Matematik {
  static const double pi = 3.14;
  static topla(double sayi1, double sayi2) {
    return sayi1 + sayi2;
  }

  static cikar(double sayi1, double sayi2) {
    return sayi1 - sayi2;
  }

  static double carp(double sayi1, double sayi2) {
    return sayi1 * sayi2;
  }

  static double bol(double sayi1, double sayi2) {
    return sayi1 / sayi2;
  }
}
